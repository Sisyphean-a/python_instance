#!/bin/bash
echo "请输入你的commit："
read be
echo "---------------添加所有修改到暂存区-------------------"
git add --all
echo "------------------------------------------------------"
echo -e "\n"
echo "---------------commit暂存区所有内容-------------------"
git commit -m "$be"
echo "------------------------------------------------------"
echo -e "\n"
echo "---------------把修改提交到github---------------------"
git push 
echo "------------------------------------------------------"
echo -e "\n"
exec /bin/bash
